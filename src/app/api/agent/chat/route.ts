export async function POST(req: NextRequest) {
  try {
    const body = await req.json()
    const { message } = body

    if (!message || typeof message !== 'string') {
      return NextResponse.json(
        { error: 'يرجى إرسال رسالة' },
        { status: 400 }
      )
    }

    // Try to use z-ai-web-dev-sdk for AI response
    let aiResponse: string | null = null

    try {
      const ZAI = (await import('z-ai-web-dev-sdk')).default
      const zai = await ZAI.create()

      const messages = [
        {
          role: 'system',
          content: 'أنت مساعد ذكي في تطبيق مساري (نظام تشغيل الحياة). أجب باللغة العربية بشكل مفيد ومختصر. ساعد المستخدم في التنظيم، الدراسة، التحفيز، والتحضير لامتحان التوجيهي. كن ودوداً ومشجعاً.',
        },
        ...(body.history || []).slice(-8),
        { role: 'user', content: message },
      ]

      const completion = await zai.chat.completions.create({
        messages,
        temperature: 0.7,
        max_tokens: 500,
      })

      aiResponse = completion.choices?.[0]?.message?.content
    } catch (aiError) {
      console.error('AI SDK error, using fallback:', aiError)
    }

    const response = aiResponse || getResponse(message)

    return NextResponse.json({ response })
  } catch (error) {
    console.error('Agent chat error:', error)
    return NextResponse.json(
      { response: getResponse('help') }
    )
  }
}
